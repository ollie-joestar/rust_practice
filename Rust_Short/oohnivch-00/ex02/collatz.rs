fn  collatz(start: u32) {
    let mut n:u32 = start;
    if n == 0 {
        return ;
    }
    while n != 1{
        println!("{}", n);
        if n%2 == 0 {
        n /= 2;
        } else {
        n = (n * 3) + 1;
        }
    }
    println!("{}", n);
}
